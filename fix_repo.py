files["src/sec_edgar_mcp/edgar_client.py"] = (
    '"""\n'
    'SEC EDGAR API client.\n'
    'Handles all HTTP communication with the SEC public EDGAR endpoints.\n'
    '"""\n'
    '\n'
    'import os\n'
    'import httpx\n'
    'from typing import Optional\n'
    'from dotenv import load_dotenv\n'
    '\n'
    'load_dotenv()\n'
    '\n'
    '_user_agent = os.getenv("SEC_USER_AGENT", "sec-edgar-mcp your-email@example.com")\n'
    '\n'
    'HEADERS = {\n'
    '    "User-Agent": _user_agent,\n'
    '    "Accept-Encoding": "gzip, deflate",\n'
    '}\n'
    '\n'
    'BASE_URL = "https://data.sec.gov"\n'
    '\n'
    '\n'
    'async def get_json(url: str, params: Optional[dict] = None) -> dict:\n'
    '    """Generic async GET request returning parsed JSON."""\n'
    '    async with httpx.AsyncClient(headers=HEADERS, timeout=30.0) as client:\n'
    '        response = await client.get(url, params=params)\n'
    '        response.raise_for_status()\n'
    '        return response.json()\n'
    '\n'
    '\n'
    'async def fetch_submissions(cik: str) -> dict:\n'
    '    """Fetch all submission metadata for a company by CIK."""\n'
    '    padded_cik = cik.zfill(10)\n'
    '    url = f"{BASE_URL}/submissions/CIK{padded_cik}.json"\n'
    '    return await get_json(url)\n'
    '\n'
    '\n'
    'async def fetch_company_concept(cik: str, taxonomy: str, tag: str) -> dict:\n'
    '    """Fetch a single XBRL concept across all filings."""\n'
    '    padded_cik = cik.zfill(10)\n'
    '    url = f"{BASE_URL}/api/xbrl/companyconcept/CIK{padded_cik}/{taxonomy}/{tag}.json"\n'
    '    return await get_json(url)\n'
    '\n'
    '\n'
    'async def search_company_by_name(name: str) -> list[dict]:\n'
    '    """Search for companies by name using EDGAR full-text search."""\n'
    '    url = "https://efts.sec.gov/LATEST/search-index"\n'
    '    params = {"q": f\'"{name}"\', "dateRange": "custom", "forms": "10-K"}\n'
    '    async with httpx.AsyncClient(headers=HEADERS, timeout=30.0) as client:\n'
    '        response = await client.get(url, params=params)\n'
    '        response.raise_for_status()\n'
    '        data = response.json()\n'
    '    hits = data.get("hits", {}).get("hits", [])\n'
    '    seen = {}\n'
    '    for hit in hits:\n'
    '        src = hit.get("_source", {})\n'
    '        entity = src.get("entity_name", "")\n'
    '        cik = src.get("file_num", "") or src.get("entity_id", "")\n'
    '        if entity and entity not in seen:\n'
    '            seen[entity] = {"name": entity, "cik": cik}\n'
    '    return list(seen.values())[:10]\n'
    '\n'
    '\n'
    'async def get_cik_from_ticker(ticker: str) -> Optional[str]:\n'
    '    """Resolve a stock ticker to a CIK using SEC company_tickers.json."""\n'
    '    url = "https://www.sec.gov/files/company_tickers.json"\n'
    '    data = await get_json(url)\n'
    '    ticker_upper = ticker.upper()\n'
    '    for _, company in data.items():\n'
    '        if company.get("ticker", "").upper() == ticker_upper:\n'
    '            return str(company["cik_str"])\n'
    '    return None\n'
)

